sub BUILD {

  my $self = shift;

  my $id      = $self->get_id;
  my $library = $self->get_library;

  if ( $id )
    {
      my $id_element = SML::Element->new
	(
	 name    => 'id',
	 library => $self->get_library,
	 value   => $id,
	);

      $library->add_property_value($id,'id',$id,$id_element);
    }

  return 1;
}

######################################################################

sub _build_sha_digest {

  my $self = shift;

  my $sha  = Digest::SHA->new;
  my $data = $self->get_content;

  $sha->add($data);

  return $sha->hexdigest;
}

######################################################################

sub _add_parts_to_list {

  my $self = shift;
  my $part = shift;
  my $list = shift;

  unless ( $part and $list )
    {
      $logger->error("CAN'T ADD PARTS TO LIST, MISSING ARGUMENTS");
      return 0;
    }

  unless ( $part->isa('SML::Part') )
    {
      $logger->error("CAN'T ADD PARTS TO LIST, PART IS NOT A PART $part");
      return 0;
    }

  foreach my $subpart (@{ $part->get_part_list })
    {
      push @{$list}, $subpart;

      if ( $subpart->contains_parts )
	{
	  $self->_add_parts_to_list($subpart,$list);
	}
    }

  return 1;
}

