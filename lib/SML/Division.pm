######################################################################

sub get_division_list {

  # Return an ordered list of divisions within this one.  Recurse to
  # provide a complete ordered sequence of divisions, depth first.

  my $self = shift;

  my $list = [];                        # division list

  no warnings 'recursion';

  foreach my $part (@{ $self->get_part_list })
    {
      if ( $part->isa('SML::Division') )
	{
	  push @{ $list }, $part;
	  push @{ $list }, @{ $part->get_division_list };
	}
    }

  return $list;
}

######################################################################

sub get_block_list {

  # Return an ordered list of blocks within this division.

  my $self = shift;

  my $list = [];                        # block list

  no warnings 'recursion';

  foreach my $part (@{ $self->get_part_list })
    {
      if ( $part->isa('SML::Block' ) )
	{
	  push @{ $list }, $part;
	}

      elsif ( $part->isa('SML::Division') )
	{
	  push @{ $list }, @{ $part->get_block_list };
	}

      else
	{
	  $logger->error("THIS SHOULD NEVER HAPPEN");
	}
    }

  return $list;
}

######################################################################

sub get_string_list {

  # Return an ordered list of strings withing this division.

  my $self = shift;

  my $list = [];                        # string list

  no warnings 'recursion';

  foreach my $block (@{ $self->get_block_list })
    {
      $self->_add_parts_to_list($block,$list);
    }

  return $list;
}

######################################################################

sub get_element_list {

  # Return an ordered list of elements within this division.

  my $self = shift;

  my $list = [];                        # element list

  foreach my $block (@{ $self->get_block_list })
    {
      if ( $block->isa('SML::Element') )
	{
	  push @{ $list }, $block;
	}

    }

  return $list;
}

######################################################################

sub get_line_list {

  my $self = shift;

  my $list = [];                        # line list

  no warnings 'recursion';

  foreach my $part (@{ $self->get_part_list })
    {
      push @{ $list }, @{ $part->get_line_list };
    }

  return $list;
}

######################################################################

sub get_first_part {

  my $self = shift;

  my $part_list = $self->get_part_list;

  unless ( $self->contains_parts )
    {
      $logger->error("CAN'T GET FIRST PART, DIVISION HAS NO PARTS");
      return 0;
    }

  return $self->get_part_list->[0];
}

######################################################################

sub get_first_line {

  my $self = shift;

  unless ( $self->contains_parts )
    {
      $logger->error("CAN'T GET FISRT LINE, DIVISION HAS NO PARTS");
      return 0;
    }

  my $first_part = $self->get_first_part;

  return $first_part->get_first_line;
}

######################################################################

sub get_containing_document {

  # Return the document to which this division belongs (or undef).

  my $self = shift;

  my $division = $self;

  while ( ref $division )
    {
      my $name = $division->get_name;

      if ( $name eq 'DOCUMENT' )
	{
	  return $division;
	}

      else
	{
	  $division = $division->get_containing_division;
	}
    }

  # $logger->error("CAN'T GET DOCUMENT");
  return 0;
}

######################################################################

sub get_location {

  # Return the location (filepec + line number) of the beginning of
  # this division.

  my $self = shift;

  my $block = $self->get_block_list->[0];

  if ( $block )
    {
      return $block->get_location;
    }

  else
    {
      return 'UNKNOWN';
    }
}

######################################################################

sub get_containing_section {

  # Return the section containing this division.  If not in a section,
  # return undef.

  my $self = shift;

  my $division = $self;

  while ( $division )
    {
      if ( $division->isa('SML::Section') )
	{
	  return $division;
	}

      else
	{
	  $division = $division->get_containing_division;
	}
    }

  # $logger->error("CAN'T GET SECTION");
  return 0;
}

######################################################################

sub is_in_a {

  # Return 1 if this division is in a division of "name" (even if it
  # is buried several divisions deep).

  my $self = shift;
  my $name = shift;

  unless ( $name )
    {
      $logger->error("CAN'T CHECK IF DIVISION IS IN ANOTHER, MISSING ARGUMENT");
      return 0;
    }

  my $division = $self;

  while ( ref $division and $division->isa('SML::Division') )
    {
      my $division_name = $division->get_name;

      if ( $division_name eq $name )
	{
	  return 1;
	}

      else
	{
	  $division = $division->get_containing_division;
	}
    }

  return 0;
}

######################################################################

sub get_content {

  my $self = shift;

  my $content = q{};

  foreach my $block (@{ $self->get_block_list })
    {
      $content .= $block->get_content . "\n\n";
    }

  return $content;
}

